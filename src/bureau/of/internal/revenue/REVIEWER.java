    public void listed() {
        String UsernameProfile = PROFILE.GetsNameReturn();
        DefaultTableModel table = new DefaultTableModel();
        table.addColumn("ID");
        table.addColumn("uniqueid");
        table.addColumn("taxpayer");
        table.addColumn("taxableyear");
        table.addColumn("loano");
        table.addColumn("examiner");
        table.addColumn("dateofloa");
        table.addColumn("dateofloareceipt");
        table.addColumn("datereceived");
        table.addColumn("groupsupervisor");

        String Dtases = ConnectionDB.GetsNameReturn();
        String DtaseN = ConnectionDB.GetDbName();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection = (Connection) DriverManager.getConnection("jdbc:mysql://" + Dtases + ":3306/" + DtaseN + "", "root", "");
            String SQL = "SELECT * FROM `docket` WHERE `REVIWER` = '" + UsernameProfile + "'";
            Statement PST = connection.createStatement();
            ResultSet resultSet = PST.executeQuery(SQL);
            while (resultSet.next()) {
                table.addRow(new Object[]{
                    resultSet.getString(1),
                    resultSet.getString(2),
                    resultSet.getString(3),
                    resultSet.getString(4),
                    resultSet.getString(5),
                    resultSet.getString(6),
                    resultSet.getString(7),
                    resultSet.getString(8),
                    resultSet.getString(9),
                    resultSet.getString(11),});
            }
            jTable1.setModel(table);
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(REVIEWER.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

