        // TODO add your handling code here:
        //UPDATE `docket` SET `groupsupervisor` = 'dada' WHERE `docket`.`id` = 2;
        String ComboValues = jComboBox1.getSelectedItem().toString();
        String ValField = jTextField1.getText();
        String UsernameProfile = PROFILE.GetsNameReturn();

        String Dtases = ConnectionDB.GetsNameReturn();
        String DtaseN = ConnectionDB.GetDbName();

        if (!"Choose".equals(ComboValues) && !"#NO".equals(ValField)) {
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection connection = (Connection) DriverManager.getConnection("jdbc:mysql://" + Dtases + ":3306/" + DtaseN + "", "root", "");
                String SQL = "SELECT * FROM `docket` WHERE `uniqueid` = '" + ValField + "' AND `REVIWER` = '" + UsernameProfile + "' ";
//                SELECT * FROM `docket` WHERE `uniqueid` = '0231' AND `REVIWER` = 'rev'
                Statement PST = connection.createStatement();
                ResultSet resultSet = PST.executeQuery(SQL);
                if (resultSet.next()) {
                    String UpSQL = "UPDATE `docket` SET `groupsupervisor` = '" + ComboValues + "' WHERE `docket`.`uniqueid` = '" + ValField + "' AND `groupsupervisor` = ''";
//                    ResultSet resultSetID = PST.executeQuery("UPDATE `docket` SET `groupsupervisor` = 's' WHERE `docket`.`uniqueid` = '" + ValField + "'");
                    if (PST.executeUpdate(UpSQL) != 0) {
                        JOptionPane.showMessageDialog(null, "Success ! ");
                    } else {
                        JOptionPane.showMessageDialog(null, "You Can't Change Group SuperVisor ! ");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Wrong ID#@2010220");
                }

            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(REVIEWER.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else if (ComboValues.equals("Choose") && ValField.equals("#NO")) {
            JOptionPane.showMessageDialog(null, "Please Input ID#@202001 && Choose GroupSuperVisor");
        } else if (ValField.equals("#NO")) {
            JOptionPane.showMessageDialog(null, "Please Input ID#@202001");
        } else if (ComboValues.equals("Choose")) {
            JOptionPane.showMessageDialog(null, "Please Choose GroupSuperVisor");
        } else {
            JOptionPane.showMessageDialog(null, "Please Contact Admin");
        }
